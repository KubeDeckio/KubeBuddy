import { mkdir, readdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import YAML from 'yaml';

const pluginRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const repoRoot = path.resolve(pluginRoot, '..');
const checksRoot = path.join(repoRoot, 'checks', 'kubernetes');
const outputPath = path.join(pluginRoot, 'src', 'generated', 'checkCatalog.ts');

const yamlFiles = (await readdir(checksRoot))
  .filter(file => file.endsWith('.yaml') && file !== 'prometheus.yaml')
  .sort();
const excludedCheckIds = new Set([
  // The CLI includes SC002 only in AKS mode. The Headlamp plugin runs the
  // generic Kubernetes browser-safe check set, so keep counts aligned.
  'SC002',
]);

function toCheck(raw, sourceFile) {
  return {
    id: raw.id,
    name: raw.name,
    category: raw.category,
    section: raw.section,
    severity: raw.severity,
    weight: raw.weight,
    description: raw.description,
    failMessage: raw.fail_message,
    recommendation: raw.recommendation,
    recommendationHtml: raw.recommendation_html,
    url: raw.url,
    resourceKind: raw.resource_kind,
    nativeHandler: raw.native_handler,
    value: raw.value,
    operator: raw.operator,
    expected: raw.expected,
    sourceFile,
  };
}

const checks = [];

for (const file of yamlFiles) {
  const raw = await readFile(path.join(checksRoot, file), 'utf8');
  const parsed = YAML.parse(raw);
  for (const check of parsed?.checks || []) {
    if (excludedCheckIds.has(String(check.id || '').toUpperCase())) {
      continue;
    }

    checks.push(toCheck(check, file));
  }
}

const body = `/* This file is generated by scripts/generate-check-catalog.mjs. Do not edit by hand. */

export type GeneratedExpression = {
  path?: string;
  value?: unknown;
  exists?: string;
  len?: GeneratedExpression;
  count_where?: {
    path: string;
    where: GeneratedPredicate;
  };
  any?: GeneratedPredicate[];
  all?: GeneratedPredicate[];
  coalesce?: GeneratedExpression[];
};

export type GeneratedPredicate = {
  path?: string;
  operator?: string;
  expected?: unknown;
  any?: GeneratedPredicate[];
  all?: GeneratedPredicate[];
};

export type GeneratedCheck = {
  id: string;
  name: string;
  category: string;
  section: string;
  severity: string;
  weight: number;
  description: string;
  failMessage: string;
  recommendation: string;
  recommendationHtml?: string;
  url?: string;
  resourceKind: string;
  nativeHandler?: string;
  value?: GeneratedExpression;
  operator?: string;
  expected?: unknown;
  sourceFile: string;
};

export const KUBERNETES_CHECKS: GeneratedCheck[] = ${JSON.stringify(checks, null, 2)};
`;

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, body, 'utf8');

console.log(`Generated ${checks.length} Kubernetes checks at ${path.relative(pluginRoot, outputPath)}`);
